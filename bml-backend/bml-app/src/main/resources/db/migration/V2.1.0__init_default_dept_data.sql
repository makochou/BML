-- 版本: V2.1.0
-- 说明: 初始化部门管理默认数据
--
-- 设计说明：
--   按照行业标准企业组织架构规范，为已有机构（深圳总公司、北京分公司）
--   创建完整的部门层级结构，涵盖以下职能分类：
--     管理、研发、销售、财务、人事、行政、生产、采购、仓储
--
-- 部门层级设计原则：
--   • 一级部门为「中心」或独立「部门」级，直属公司（parent_id=0）
--   • 二级部门为标准「部门」级，挂在对应中心下
--   • 三级单元为「小组」级，适用于研发等需要细分的场景
--   • 每个部门分配唯一 dept_code，格式：{机构缩写}_{职能缩写}_{编号}
--
-- 数据关联：
--   • org_id=101 → 深圳总公司（主要经营实体，部门完整）
--   • org_id=102 → 北京分公司（分支机构，部门精简）
--
-- ID 分配规则（便于识别与扩展）：
--   • 深圳总公司部门 ID: 1000 ~ 1999
--   • 北京分公司部门 ID: 2000 ~ 2999
--   • 预留 3000+ 给后续新增机构
--
-- 幂等性：
--   使用 INSERT IGNORE 避免重复执行时报错
-- =============================================================================

