-- V1.9.1 数据隔离模式扩展 — 从 2 种扩展为 5 种
-- ═══════════════════════════════════════════════════════════════════════════════
-- 说明：
--   V1.9.0 中 sys_org.data_isolation 仅支持 0(共享) 和 1(隔离) 两种模式。
--   本脚本将 data_isolation 字段注释更新为 5 种模式，并确保默认值不变。
--   TINYINT 类型本身已支持 0~4，无需变更列类型。
--
--   五种数据隔离模式：
--     0 — 共享（上级穿透）：上级可查看下级数据，默认模式
--     1 — 完全隔离：各机构数据独立，上下级不可互查
--     2 — 汇总共享：上级仅可查看下级汇总数据，不可查看明细
--     3 — 同级互通：兄弟机构可互查数据，上级也可查看
--     4 — 按模块隔离：部分模块隔离、部分共享（需配合模块配置表，预留扩展）
-- ═══════════════════════════════════════════════════════════════════════════════

-- 兼容旧版 V1.9.0（可能未包含 data_isolation 列）：先确保列存在
ALTER TABLE `sys_org`
    ADD COLUMN IF NOT EXISTS `data_isolation` TINYINT NOT NULL DEFAULT 0
    COMMENT '数据隔离模式 (0:共享 1:完全隔离 2:汇总共享 3:同级互通 4:按模块隔离)';

-- 更新列注释（无论列是旧建还是新建，统一为 5 种模式说明）
ALTER TABLE `sys_org`
    MODIFY COLUMN `data_isolation` TINYINT NOT NULL DEFAULT 0
    COMMENT '数据隔离模式 (0:共享 1:完全隔离 2:汇总共享 3:同级互通 4:按模块隔离)';
