-- BML 企业管理系统 — 数据库迁移脚本
-- 版本: V2.2.0
-- 说明: 角色权限授权体系增强
--
-- 变更内容：
--   1. sys_role_menu 新增 half_check 字段，支持树形半选（indeterminate）状态持久化
--   2. sys_menu 新增 menu_type='F'（字段权限）示例数据，统一菜单/按钮/字段三级授权
--
-- 设计说明：
--   前端权限树使用 Arco Design a-tree 的 checkable 模式（非 check-strictly），
--   父节点的勾选状态由子节点自动推导。保存时需要区分：
--     • 完全勾选节点（half_check=0）：代表该权限已授予
--     • 半选节点（half_check=1）：仅部分子权限授予，父节点为 indeterminate 状态
--   加载时只把 half_check=0 的节点设为 checkedKeys，树组件自动计算半选态。
--
-- 字段权限说明：
--   在 sys_menu 中新增 menu_type='F' 类型节点，挂在对应菜单（C 类型）下方，
--   与按钮（B 类型）同级，统一通过 sys_role_menu 管理授权关系。
--   权限标识格式：{模块}:{资源}:field:{字段名}，如 system:user:field:phone
-- =============================================================================

-- ═══════════════════════════════════════════════════════════════════════════════
-- 一、sys_role_menu 新增 half_check 字段
-- ═══════════════════════════════════════════════════════════════════════════════

ALTER TABLE `sys_role_menu`
    ADD COLUMN `half_check` TINYINT NOT NULL DEFAULT 0
        COMMENT '是否半选状态：0 完全勾选（权限已授予），1 半选（仅部分子权限授予）';

-- ═══════════════════════════════════════════════════════════════════════════════
-- 二、sys_menu 新增字段权限示例数据（menu_type='F'）
-- ═══════════════════════════════════════════════════════════════════════════════
--
-- 字段权限节点挂在对应菜单下方，与按钮同级：
--
-- 用户管理 (id=41, C)
-- ├── 用户查询 (id=411, B)
-- ├── 用户新增 (id=412, B)
-- ├── 用户编辑 (id=413, B)
-- ├── 用户删除 (id=414, B)
-- ├── 重置密码 (id=415, B)
-- ├── 手机号码 (id=416, F)  ← 新增
-- ├── 邮箱地址 (id=417, F)  ← 新增
-- └── 真实姓名 (id=418, F)  ← 新增
--
-- 角色管理 (id=42, C)
-- ├── 角色查询 (id=421, B)
-- ├── 角色新增 (id=422, B)
-- ├── 角色编辑 (id=423, B)
-- └── 角色删除 (id=424, B)
-- （角色管理暂无字段权限需求，保持不变）
--
-- ═══════════════════════════════════════════════════════════════════════════════

-- 用户管理 — 字段权限
INSERT INTO `sys_menu` (`id`, `menu_name`, `parent_id`, `menu_type`, `perms`, `sort`, `visible`, `status`, `remark`, `create_by`, `create_time`)
VALUES
    (416, '手机号码', 41, 'F', 'system:user:field:phone',    10, 1, 1, '控制用户列表和详情中手机号码字段的可见性', 1, NOW()),
    (417, '邮箱地址', 41, 'F', 'system:user:field:email',    11, 1, 1, '控制用户列表和详情中邮箱地址字段的可见性', 1, NOW()),
    (418, '真实姓名', 41, 'F', 'system:user:field:realName', 12, 1, 1, '控制用户列表和详情中真实姓名字段的可见性', 1, NOW())
ON DUPLICATE KEY UPDATE menu_name = VALUES(menu_name);

-- 超级管理员赋予新增字段权限
INSERT INTO `sys_role_menu` (`role_id`, `menu_id`, `half_check`)
SELECT 1, id, 0 FROM `sys_menu` WHERE `id` IN (416, 417, 418)
ON DUPLICATE KEY UPDATE `half_check` = 0;

