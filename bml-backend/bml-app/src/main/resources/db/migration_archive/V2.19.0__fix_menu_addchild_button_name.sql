-- 说明: 修复菜单管理"新增子菜单"按钮名称，对齐前端页面实际按钮文案"新增下级"
--
-- 变更背景：
--   菜单管理页面操作列中的按钮文案为"新增下级"，但数据库 sys_menu 表中
--   对应的 B 类型权限项（id=4005）名称为"新增子菜单"，导致功能授权面板
--   中显示的按钮名称与前端页面实际文案不一致。
--   本次迁移将数据库中的名称统一为前端实际使用的"新增下级"。
--
-- 影响范围：
--   - 功能授权弹窗中"菜单管理"分组下的按钮名称显示
--   - 不影响权限标识（perms）和功能逻辑
-- ═══════════════════════════════════════════════════════════════════════════════

-- 将"新增子菜单"重命名为"新增下级"（对齐前端菜单管理页面操作列按钮文案）
UPDATE `sys_menu`
SET `menu_name` = '新增下级',
    `remark`    = '菜单管理 — 新增下级按钮（操作列）'
WHERE `id` = 4005
  AND `menu_type` = 'B'
  AND `perms` = 'system:menu:addChild';
