--   1. 删除主题预设表 bml_theme_preset
--   2. 删除用户主题设置表 bml_theme_user_setting
--   3. 删除主题管理菜单及其按钮权限（id=74, 7401, 7402）
--   4. 删除超级管理员对主题菜单的角色授权
-- =============================================================================

-- ═══════════════════════════════════════════════════════════════════════════════
-- 一、删除角色-菜单关联（主题相关）
-- ═══════════════════════════════════════════════════════════════════════════════
DELETE FROM `sys_role_menu` WHERE `menu_id` IN (74, 7401, 7402);

-- ═══════════════════════════════════════════════════════════════════════════════
-- 二、删除主题管理菜单及按钮权限
-- ═══════════════════════════════════════════════════════════════════════════════
DELETE FROM `sys_menu` WHERE `id` IN (74, 7401, 7402);

-- ═══════════════════════════════════════════════════════════════════════════════
-- 三、删除主题相关数据表
-- ═══════════════════════════════════════════════════════════════════════════════
DROP TABLE IF EXISTS `bml_theme_user_setting`;
DROP TABLE IF EXISTS `bml_theme_preset`;
