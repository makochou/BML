-- ─────────────────────────────────────────────────────────────────
-- 一、重命名"权限分配"为"功能授权"（对齐前端工具栏按钮文案）
-- ─────────────────────────────────────────────────────────────────
UPDATE `sys_menu`
SET `menu_name` = '功能授权',
    `remark`    = '角色与权限 — 功能授权按钮（工具栏）'
WHERE `id` = 4004;

-- ─────────────────────────────────────────────────────────────────
-- 二、新增"绑定用户"按钮权限（前端已使用 system:role:assignUser）
-- ─────────────────────────────────────────────────────────────────
-- parent_id = 42（角色与权限菜单）
-- ID 使用 4007 避免与已有扩展按钮 ID 冲突（4001-4006 已占用）
INSERT IGNORE INTO `sys_menu` (`id`, `menu_name`, `parent_id`, `menu_type`, `perms`, `sort`, `visible`, `status`, `remark`, `create_by`, `create_time`)
VALUES (4007, '绑定用户', 42, 'B', 'system:role:assignUser', 6, 1, 1, '角色与权限 — 绑定用户按钮（工具栏）', 1, NOW());
