package com.bml.core.common.constant;

/**
 * 全局常量定义
 * <p>
 * 集中管理项目中所有通用常量，避免魔法值散落在业务代码中。
 * 新增常量时请按类别分组，并附上中文注释说明用途。
 * </p>
 *
 * @author BML Team
 */
public interface GlobalConstants {

    // ======================== 链路追踪 ========================

    /**
     * MDC 链路追踪ID的 Key
     */
    String TRACE_ID = "traceId";

    // ======================== 系统用户 ========================

    /**
     * 超级管理员用户ID
     * <p>
     * 数据库中超级管理员的固定 ID，用于判断是否拥有全部权限。
     * 与 {@code SysUserServiceImpl.checkUserAllowed()} 中的保护逻辑保持一致。
     * </p>
     */
    Long SYSTEM_USER_ID = 1L;

    // ======================== 树形结构 ========================

    /**
     * 树形结构根节点ID
     * <p>
     * 菜单树、部门树等树形结构中，顶级节点的 parentId 约定为 0。
     * </p>
     */
    Long ROOT_NODE_ID = 0L;

    // ======================== 状态标识 ========================

    /**
     * 正常状态（状态值: 1）
     * <p>
     * 适用于：用户状态、角色状态、菜单状态、部门状态等
     * </p>
     */
    Integer STATUS_NORMAL = 1;

    /**
     * 停用状态（状态值: 0）
     * <p>
     * 适用于：用户状态、角色状态、菜单状态、部门状态等
     * </p>
     */
    Integer STATUS_DISABLE = 0;

    // ======================== 逻辑删除 ========================

    /**
     * 删除标记: 未删除（值: 0）
     */
    Integer NOT_DELETED = 0;

    /**
     * 删除标记: 已删除（值: 1）
     */
    Integer DELETED = 1;
}
